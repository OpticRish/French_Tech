const themeToggleBtn = document.getElementById('theme-toggle');
const langToggleBtn = document.getElementById('lang-toggle');

if (themeToggleBtn) {
    const themeIcon = themeToggleBtn.querySelector('i');
    themeToggleBtn.addEventListener('click', () => {
        const isDark = document.body.classList.toggle('dark-mode');
        localStorage.setItem('appTheme', isDark ? 'dark' : 'light');
        if (themeIcon) {
            themeIcon.classList.toggle('fa-sun', !isDark);
            themeIcon.classList.toggle('fa-moon', isDark);
        }
    });
}

if (langToggleBtn) {
    const langLabel = document.getElementById('current-lang');
    langToggleBtn.addEventListener('click', () => {
        currentLang = currentLang === 'EN' ? 'FR' : 'EN';
        localStorage.setItem('appLang', currentLang);
        if (langLabel) langLabel.textContent = currentLang;
        updateLanguage();
    });
}

function updateLanguage() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (i18n[currentLang] && i18n[currentLang][key]) {
            el.innerHTML = i18n[currentLang][key];
        }
    });
}

window.setAppLang = function(lang) {
    currentLang = lang;
    updateLanguage();
};

// ─── Counter Animation ───────────────────────────────────────────────────────
function animateCounter(el) {
    const target = parseInt(el.getAttribute('data-counter'), 10);
    const suffix = el.getAttribute('data-suffix') || '';
    const duration = 1800;
    const startTime = performance.now();
    const isYear = target > 1900 && target < 2100;

    function easeOut(t) { return 1 - Math.pow(1 - t, 3); }

    function update(now) {
        const elapsed = now - startTime;
        const progress = Math.min(elapsed / duration, 1);
        const eased = easeOut(progress);
        const current = isYear
            ? Math.round(1900 + (target - 1900) * eased)
            : Math.round(target * eased);

        // Format large numbers with commas
        el.textContent = current.toLocaleString() + suffix;

        if (progress < 1) requestAnimationFrame(update);
        else el.textContent = target.toLocaleString() + suffix;
    }

    requestAnimationFrame(update);
}

// ─── Particle Burst ──────────────────────────────────────────────────────────
function createParticleBurst(x, y) {
    const colors = ['#818cf8', '#f472b6', '#60a5fa', '#fbbf24', '#4ade80'];
    for (let i = 0; i < 16; i++) {
        const p = document.createElement('div');
        p.className = 'particle';
        const size = Math.random() * 8 + 4;
        const angle = (i / 16) * Math.PI * 2;
        const dist = Math.random() * 80 + 40;
        const tx = Math.cos(angle) * dist;
        const ty = Math.sin(angle) * dist;
        p.style.cssText = `
            width:${size}px; height:${size}px;
            left:${x}px; top:${y}px;
            background:${colors[Math.floor(Math.random() * colors.length)]};
            --tx:${tx}px; --ty:${ty}px;
            animation-delay:${Math.random() * 0.1}s;
        `;
        document.body.appendChild(p);
        setTimeout(() => p.remove(), 900);
    }
}

// ─── Scroll Animations ───────────────────────────────────────────────────────
const observerOptions = { root: null, rootMargin: '0px', threshold: 0.1 };
const observer = new IntersectionObserver((entries, obs) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('visible');
            obs.unobserve(entry.target);
        }
    });
}, observerOptions);

// Counter observer (separate, triggers counting when visible)
const counterObserver = new IntersectionObserver((entries, obs) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            animateCounter(entry.target);
            obs.unobserve(entry.target);
        }
    });
}, { root: null, rootMargin: '0px', threshold: 0.2 });

