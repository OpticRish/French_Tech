document.addEventListener('DOMContentLoaded', () => {
    // Observe scroll animations
    document.querySelectorAll('.observer-item').forEach(el => observer.observe(el));

    // Observe counter elements
    document.querySelectorAll('[data-counter]').forEach(el => counterObserver.observe(el));

    // Init language
    updateLanguage();

    // Explore button
    const exploreBtn = document.getElementById('explore-btn') || document.querySelector('.primary-btn');
    const contentSection = document.getElementById('content-section');
    if (exploreBtn && contentSection) {
        exploreBtn.addEventListener('click', (e) => {
            createParticleBurst(e.clientX, e.clientY);
            setTimeout(() => {
                contentSection.scrollIntoView({ behavior: 'smooth' });
            }, 100);
        });
    }

    // ─── Scroll Progress Bar ────────────────────────────────────────────────────
    const progressContainer = document.createElement('div');
    progressContainer.className = 'scroll-progress-container';
    const progressBar = document.createElement('div');
    progressBar.className = 'scroll-progress-bar';
    progressContainer.appendChild(progressBar);
    document.body.appendChild(progressContainer);

    window.addEventListener('scroll', () => {
        const totalHeight = document.documentElement.scrollHeight - window.innerHeight;
        const progress = (window.scrollY / totalHeight) * 100;
        progressBar.style.width = `${progress}%`;
    });

    // Scroll video bg fade
    const scrollVideoBg = document.getElementById('scroll-video-bg');
    if (scrollVideoBg) {
        window.addEventListener('scroll', () => {
            scrollVideoBg.style.opacity = window.scrollY > window.innerHeight * 0.4 ? '1' : '0';
        });
    }

    // ─── Rafale Spotlight (fallback, rafale.html has its own enhanced version) ───
    const rafaleBox = document.getElementById('rafale-container');
    const revealImg = document.getElementById('reveal-img');
    if (rafaleBox && revealImg && !rafaleBox.dataset.enhanced) {
        rafaleBox.dataset.enhanced = 'true';
        rafaleBox.addEventListener('mousemove', (e) => {
            const rect = rafaleBox.getBoundingClientRect();
            revealImg.style.setProperty('--mx', `${e.clientX - rect.left}px`);
            revealImg.style.setProperty('--my', `${e.clientY - rect.top}px`);
            revealImg.style.opacity = '1';
        });
        rafaleBox.addEventListener('mouseleave', () => {
            revealImg.style.setProperty('--mx', '-9999px');
            revealImg.style.setProperty('--my', '-9999px');
            revealImg.style.opacity = '0.2';
        });
    }

    // ─── Custom Jet Cursor ───────────────────────────────────────────────────
    const cursorJet = document.createElement('div');
    cursorJet.id = 'custom-cursor-jet';
    cursorJet.innerHTML = `
        <svg viewBox="0 0 100 100" width="40" height="40">
           <defs>
              <linearGradient id="jetGradient" x1="0%" y1="0%" x2="0%" y2="100%">
                 <stop offset="0%" style="stop-color:#f472b6;stop-opacity:1" />
                 <stop offset="100%" style="stop-color:#818cf8;stop-opacity:1" />
              </linearGradient>
           </defs>
           <polygon points="50,5 42,30 42,85 50,95 58,85 58,30" fill="url(#jetGradient)" />
           <polygon points="42,45 10,75 42,75" fill="url(#jetGradient)" />
           <polygon points="58,45 90,75 58,75" fill="url(#jetGradient)" />
           <polygon points="42,25 25,35 42,35" fill="url(#jetGradient)" />
           <polygon points="58,25 75,35 58,35" fill="url(#jetGradient)" />
        </svg>
    `;
    cursorJet.style.color = '#fff'; // Bright white base for filtering
    document.body.appendChild(cursorJet);

    const abner = document.createElement('div');
    abner.className = 'afterburner';
    document.body.appendChild(abner);

    let mouseX = window.innerWidth / 2;
    let mouseY = window.innerHeight / 2;
    let jetX = mouseX, jetY = mouseY, angle = 0, frameCount = 0;

    window.addEventListener('mousemove', (e) => { mouseX = e.clientX; mouseY = e.clientY; });

    function createCloud(x, y) {
        const cloud = document.createElement('div');
        cloud.className = 'cloud-trail';
        cloud.style.left = `${x}px`;
        cloud.style.top = `${y}px`;
        document.body.appendChild(cloud);
        setTimeout(() => cloud.remove(), 600);
    }

    function animateJet() {
        const dx = mouseX - jetX;
        const dy = mouseY - jetY;
        const dist = Math.sqrt(dx*dx + dy*dy);
        
        jetX += dx * 0.18;
        jetY += dy * 0.18;

        if (dist > 1) {
            const targetAngle = Math.atan2(dy, dx) * (180 / Math.PI) + 90;
            let angleDiff = targetAngle - angle;
            if (angleDiff > 180) angleDiff -= 360;
            if (angleDiff < -180) angleDiff += 360;
            angle += angleDiff * 0.18;

            frameCount++;
            if (frameCount % 2 === 0) {
                const moveRad = (angle - 90) * (Math.PI / 180);
                // Dual trails
                createCloud(
                    jetX - Math.cos(moveRad) * 18 + Math.cos(moveRad - Math.PI/2) * 8,
                    jetY - Math.sin(moveRad) * 18 + Math.sin(moveRad - Math.PI/2) * 8
                );
                createCloud(
                    jetX - Math.cos(moveRad) * 18 + Math.cos(moveRad + Math.PI/2) * 8,
                    jetY - Math.sin(moveRad) * 18 + Math.sin(moveRad + Math.PI/2) * 8
                );
            }
            
            // Afterburner logic
            abner.style.opacity = dist > 40 ? '1' : '0';
            abner.style.transform = `translate(${jetX}px, ${jetY}px) rotate(${angle}deg) scale(${Math.min(dist/100, 1.5)}) translateY(20px)`;
        } else {
            abner.style.opacity = '0';
        }

        cursorJet.style.transform = `translate(${jetX}px, ${jetY}px) rotate(${angle}deg)`;
        requestAnimationFrame(animateJet);
    }
    animateJet();
});

