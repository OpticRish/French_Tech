window.getHistoryText = function(key) {
    return (historyData[currentLang] && historyData[currentLang][key]) || key;
};

function updateHistoryPage() {
    document.querySelectorAll('[data-history-i18n]').forEach(el => {
        const key = el.getAttribute('data-history-i18n');
        el.textContent = window.getHistoryText(key);
    });
}

// Hook into the global translation update
const originalUpdateLanguage = window.updateLanguage;
window.updateLanguage = function() {
    if (originalUpdateLanguage) originalUpdateLanguage();
    updateHistoryPage();
};
